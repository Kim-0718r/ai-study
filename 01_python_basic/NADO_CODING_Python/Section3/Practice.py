print(1+1) # 2
print(3-2) # 1
print(5*2) # 10
print(6/3) # 2
print(2**3) # 2^3 = 8
print(5%3) # 나머지 구하기 2
print(10%3) # 나머지 구하기 1
print(5//3) # 몫 구하기
print(10//3) # 3
print(10 > 3) # True
print(4 >= 7) # False 
print(5 <= 5) # True
print(10 < 3) # False

print(3 == 3) # 앞에 있는 값과 뒤에 있는 값이 같다. True
print(4 == 2) # False
print(3 + 4 == 7) # True
print(1 != 3) # 앞 뒤가 서로 다르다 True
print(not(1 != 3)) # True의 not인 False
print((3 > 0) and (3 < 5)) # True and는 앞과 뒤가 다 True 여만 True
print((3 > 0) & (3 < 5)) # & < 이것도 and
print((3>0) or (3>5)) # or 둘중 하나만 True 여도 True
print((3>0) | (3 > 5)) # | < 이것도 or 
print(5 > 4> 3) # True
print(5 > 4 > 7) # False
